/**
 * 第一个java类
 */
public class HelloWorld {
    //java的文件名和类名是一样的
    public static void main(String[] args) {        //psvm
        //java的入口函数      一个类文件只能写一个入口函数
        System.out.println("Hello World!!!");           //sout     输出语句
        System.out.print("你好\n");


        /*
        System.out.println();       输出信息之后换行
        System.out.print();         输出信息之后不换行   s
         */
        System.out.println("\t\t静夜思");
        System.out.println("\t\t\t\t李白");
        System.out.print("床前明月光，");
        System.out.println("疑是地上霜。");
        System.out.print("举头望明月，");
        System.out.println("低头思故乡。");

        //java注释：  3种
        //单行注释
        /*
        * 多行注释
        * */

        /**
         * 文档注释：  打在类名和方法名上面
         */
    }

}
